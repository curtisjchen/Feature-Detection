class HarrisKeypointDetector(KeypointDetector):

    # Compute harris values of an image.
    def computeHarrisValues(self, srcImage):
        '''
        Input:
            srcImage -- Grayscale input image in a numpy array with
                        values in [0, 1]. The dimensions are (rows, cols).
        Output:
            harrisImage -- numpy array containing the Harris score at
                           each pixel.
            orientationImage -- numpy array containing the orientation of the
                                gradient at each pixel in degrees.
        '''

        # Compute x and y derivatives of image
        Ix = cv2.Sobel(srcImage, cv2.CV_64F, 1, 0, ksize=3, borderType=cv2.BORDER_REFLECT)
        Iy = cv2.Sobel(srcImage, cv2.CV_64F, 0, 1, ksize=3, borderType=cv2.BORDER_REFLECT)

        # Compute products of derivatives at every pixel
        Ixx = Ix ** 2
        Ixy = Ix * Iy
        Iyy = Iy ** 2

        # Compute the sum of the products of derivatives at each pixel
        Sxx = cv2.GaussianBlur(Ixx, (5, 5), sigmaX=0.5, borderType=cv2.BORDER_REFLECT)
        Sxy = cv2.GaussianBlur(Ixy, (5, 5), sigmaX=0.5, borderType=cv2.BORDER_REFLECT)
        Syy = cv2.GaussianBlur(Iyy, (5, 5), sigmaX=0.5, borderType=cv2.BORDER_REFLECT)

        # Compute determinant and trace of the product matrix
        det = (Sxx * Syy) - (Sxy ** 2)
        trace = Sxx + Syy

        # Compute the Harris corner strength at each pixel
        k = 0.1
        harrisImage = det - k * (trace ** 2)

        # Compute the orientation of the gradient at each pixel
        orientationImage = np.degrees(np.arctan2(Iy, Ix))

        return harrisImage, orientationImage

    def computeLocalMaxima(self, harrisImage):
        '''
        Input:
            harrisImage -- numpy array containing the Harris score at
                           each pixel.
        Output:
            destImage -- numpy array containing True/False at
                         each pixel, depending on whether
                         the pixel value is the local maximum in
                         its 7x7 neighborhood.
        '''
        destImage = np.zeros_like(harrisImage, np.bool)

        destImage = np.zeros_like(harrisImage, np.bool)

        # Find the maximum value in each 7x7 neighborhood
        maxImage = cv2.dilate(harrisImage, np.ones((7, 7)))

        # Set pixels in destImage to True if they are the maximum in their neighborhood
        destImage = (harrisImage >= maxImage)

        return destImage

    def detectKeypoints(self, image):
        '''
        Input:
            image -- BGR image with values between [0, 255]
        Output:
            list of detected keypoints, fill the cv2.KeyPoint objects with the
            coordinates of the detected keypoints, the angle of the gradient
            (in degrees), the detector response (Harris score for Harris detector)
            and set the size to 10.
        '''
        image = image.astype(np.float32)
        image /= 255.
        height, width = image.shape[:2]
        features = []

        # Create grayscale image used for Harris detection
        grayImage = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)

        # computeHarrisValues() computes the harris score at each pixel
        # position, storing the result in harrisImage.
        # You will need to implement this function.
        harrisImage, orientationImage = self.computeHarrisValues(grayImage)

        # Compute local maxima in the Harris image.  You will need to
        # implement this function. Create image to store local maximum harris
        # values as True, other pixels False
        harrisMaxImage = self.computeLocalMaxima(harrisImage)

        # Loop through feature points in harrisMaxImage and fill in information
        # needed for descriptor computation for each point.
        # You need to fill x, y, and angle.
        for y in range(height):
            for x in range(width):
                if not harrisMaxImage[y, x]:
                    continue

                f = cv2.KeyPoint()

                # TODO 3: Fill in feature f with location and orientation
                # data here. Set f.size to 10, f.pt to the (x,y) coordinate,
